class Variables {
    public static void main(String[] args) {
        int Passengers = 0;
        System.out.println(Passengers);

        int busTicket = Passengers + 9;
        System.out.println(busTicket);

        busTicket -= 4;
        System.out.println(busTicket);

        busTicket += 10;
        System.out.println(busTicket);

        busTicket *= 5;
        System.out.println(busTicket);

        busTicket /= 2;
        System.out.println(busTicket);

    }
}
