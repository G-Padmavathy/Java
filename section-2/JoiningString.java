class JoiningString {
    public static void main(String[] args) {
        String a = "Hello";
        String b = "Padmavathy";

        System.out.println(a + " " + b);

        char Apple = 'A';
        char Bat = 'B';
        char Cat = 'C';
        System.out.println("Apple for " + Apple);
        System.out.println("Bat for " + Bat);
        System.out.println("Cat for " + Cat);
    }
}